package com.keeper.company.dwkeeper;

public class FichaView {

    public int id;
    public String nome;
    public String classe;
    public String nivel;
    public String img;

    public FichaView(int id, String nome, String classe, String nivel, String img) {
        this.id = id;
        this.nome = nome;
        this.classe = classe;
        this.nivel = nivel;
        this.img = img;
    }
}
